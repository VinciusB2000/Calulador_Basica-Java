package aula;

public class Calculos {
	
	public double soma(double n1, double n2){
		double total = n1+n2;
		return total;
}
	public double subtracao(double n1, double n2){
		double total = n1-n2;
		return total;
}
	public double multplicacao(double n1, double n2){
		double total = n1*n2;
		return total;
}
	public double divisao(double n1, double n2){
		double total = n1/n2;
		return total;
}


}
